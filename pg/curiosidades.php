		<p class="noticiaP2">
			A evasão no ensino superior é um fenômeno que tem chamado a atenção de gestores e pesquisadores nas últimas décadas. Observa-se na literatura internacional a tentativa de elaboração de diversos modelos teóricos sobre evasão e persistência no ensino superior. Essas pesquisas internacionais, de um modo geral, 
			são elaboradas por pesquisadores que têm como foco o estudo e a avaliação do ensino superior. No Brasil, as pesquisas em evasão são realizadas por pesquisadores dentro de suas próprias áreas. Elas tendem a ser descritivas e exploratórias, focadas no diagnóstico específico de um curso ou instituição. Geralmente, não há um aprofundamento teórico e quando há tende-se a utilizar a teoria de Vincent Tinto como referencial. Contudo, estudos nacionais e internacionais têm sugerido que essa teoria é insuficiente para dar conta do fenômeno. Dado esse quadro, este artigo tem o objetivo de apresentar outros modelos de evasão universitária e discutir os rumos das pesquisas sobre evasão no Brasil. Conclui-se que, para melhorar a qualidade das pesquisas realizadas no Brasil é necessária a elaboração de um modelo de evasão aplicável ao contexto nacional.

