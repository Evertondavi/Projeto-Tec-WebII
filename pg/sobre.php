		<p class="sobreP1">O QUE É?</p>
		<p class="sobreP2">
Atendimento psicológico on-line é um serviço psicológico realizado à distância, que se utiliza de meios tecnológicos e tem a função de informar, orientar e esclarecer - focal, breve e pontualmente, questões ou dúvidas pessoais vivenciadas pelo cliente. Objetiva proporcionar ao cliente a oportunidade de refletir acerca de si mesmo, seus comportamentos e atitudes diante dos outros e frente às situações de seu cotidiano, buscando o desenvolvimento de seus recursos pessoais e, consequentemente, de formas e estratégias mais operativas no enfrentamento das questões e conflitos que experimenta. O atendimento psicológico on-line segue rigorosamente as normas do Código de Ética Profissional do Psicólogo e é realizado por psicólogos formados, inscritos no Conselho Regional de Psicologia que, com o CFP, credencia os sites habilitados à prestação de tais serviços e fiscaliza os procedimentos adotados pelos mesmos. 
        </p>
</article>

<article class="sobre">
		<p class="sobreP1">A QUEM SE DESTINA?</p>
		<p class="sobreP2">
		
A pessoas maiores de dezoito anos* que atravessam situações pontuais em que um conflito ou sofrimento esteja presente, relacionados com a vida afetiva, sexual e familiar, estados de ansiedade e/ou depressão, dificuldade de integração social, perdas, rompimentos e mudanças em decorrência de situações profissionais, doenças pessoais ou de familiares e pessoas próximas, que causem impacto ou mudanças no relacionamento para consigo mesmo ou para com os outros. Além disso, podem se beneficiar do atendimento on-line, pessoas com dificuldade de deambulação e/ou locomoção, pessoas em trânsito ou que residam em outros países em que a língua estrangeira se constitua como barreira temporária para o acolhimento de sua demanda pelos serviços de saúde locais, bem como pessoas que residam em municípios em que a oferta de serviços psicológicos se demonstre ausente ou iinsuficiente
        </p>
		
</article>

